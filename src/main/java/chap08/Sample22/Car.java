package chap08.Sample22;

public class Car {
	
	private String	color;			// 차량색
	private int 	doors;			// 문 수
	private String 	maunfacturer;	// 제조사
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getDoors() {
		return doors;
	}
	public void setDoors(int doors) {
		this.doors = doors;
	}
	public String getMaunfacturer() {
		return maunfacturer;
	}
	public void setMaunfacturer(String maunfacturer) {
		this.maunfacturer = maunfacturer;
	}
	

}
